#mostre o tipo primitivo:

a = input ('Digite algo:')
print('O tipo primitivo desse valor é', type (a))
print('Ele so tem espaço', a.isspace())
print('È um numero', a.isnumeric())
print('E alfabetico?', a.isalpha())
print('E alfanumerico?', a.isalnum())
print ('Está em maiusculas ?', a.isupper())
print ('Esta em minusculo?', a.islower())
print('Esta capitalizada?' , a.istitle())